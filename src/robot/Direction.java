package robot;

/**
 * This class contains 8 directions to make code easier to read. When theta is
 * 0, the direction is NORTH. When theta is 45, the direction is NORTHWEST...
 * etc
 * 
 * @author Kevin Musgrave
 * 
 */

public enum Direction {

	NORTH, NORTHWEST, WEST, SOUTHWEST, SOUTH, SOUTHEAST, EAST, NORTHEAST

}
